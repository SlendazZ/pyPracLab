            "## Handling rate limits (429)\n\n"
            "```python\n"
            "import time\n\n"
            "def get_with_backoff(session, url, max_retries=3):\n"
            "    for attempt in range(max_retries):\n"
            "        r = session.get(url, timeout=10)\n"
            "        if r.status_code != 429:\n"
            "            r.raise_for_status()\n"
            "            return r\n"
            "        wait = int(r.headers.get('Retry-After', 2 ** attempt))\n"
            "        time.sleep(wait)\n"
            "    raise RuntimeError('rate limited after retries')\n"
            "```\n\n"
