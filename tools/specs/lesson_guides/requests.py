            "## Retries with urllib3\n\n"
            "Transient server errors (502, 503, 504) often resolve on retry. Mount "
            "a retry adapter on the session:\n\n"
            "```python\n"
            "from requests.adapters import HTTPAdapter\n"
            "from urllib3.util.retry import Retry\n\n"
            "retry = Retry(\n"
            "    total=3,\n"
            "    backoff_factor=0.5,\n"
            "    status_forcelist=[502, 503, 504],\n"
            "    allowed_methods=['GET', 'HEAD', 'OPTIONS'],\n"
            ")\n"
            "adapter = HTTPAdapter(max_retries=retry)\n"
            "session = requests.Session()\n"
            "session.mount('https://', adapter)\n"
            "session.mount('http://', adapter)\n"
            "```\n\n"
