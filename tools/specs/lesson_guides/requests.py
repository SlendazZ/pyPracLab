            "# Sessions, Cookies, and Retries\n\n"
            "## Overview\n\n"
            "A `requests.Session` is a persistent client: it keeps headers, cookies, "
            "and TCP connections across multiple requests. Use it whenever you talk to "
            "the same host more than once — it is faster and mirrors how browsers work.\n\n"
            "Real-world uses:\n\n"
            "- Authenticated API clients (Bearer token on every call)\n"
            "- Cookie-based login flows (session ID after POST /login)\n"
            "- Scraping sites that set cookies on first visit\n"
            "- Resilient integrations that retry transient 502/503 errors\n\n"
            "## Default headers on a session\n\n"
            "```python\n"
            "import requests\n\n"
            "session = requests.Session()\n"
            "session.headers['Authorization'] = f'Bearer {token}'\n"
            "session.headers['Accept'] = 'application/json'\n"
            "session.headers['User-Agent'] = 'myapp/2.1'\n\n"
            "r = session.get('https://api.example.com/me', timeout=10)\n"
            "```\n\n"
            "Per-request headers merge with session defaults. A call-level header "
            "overrides the session value for that request only:\n\n"
            "```python\n"
            "session.get(url, headers={'X-Request-Id': 'abc-123'}, timeout=10)\n"
            "```\n\n"
            "**Worked example:** API client class:\n\n"
            "```python\n"
            "class ApiClient:\n"
            "    def __init__(self, base_url: str, token: str):\n"
            "        self.session = requests.Session()\n"
            "        self.session.headers.update({\n"
            "            'Authorization': f'Bearer {token}',\n"
            "            'Accept': 'application/json',\n"
            "        })\n"
            "        self.base_url = base_url.rstrip('/')\n\n"
            "    def get_user(self, user_id: int) -> dict:\n"
            "        r = self.session.get(f'{self.base_url}/users/{user_id}', timeout=10)\n"
            "        r.raise_for_status()\n"
            "        return r.json()\n\n"
            "    def create_user(self, payload: dict) -> dict:\n"
            "        r = self.session.post(f'{self.base_url}/users', json=payload, timeout=10)\n"
            "        r.raise_for_status()\n"
            "        return r.json()\n"
