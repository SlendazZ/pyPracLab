            "def health(request):\n"
            "    return {'status': 200, 'body': 'ok'}\n\n"
            "def not_found(request):\n"
            "    return {'status': 404, 'body': 'not found'}\n\n"
            "routes = {\n"
            "    ('GET', '/'): home,\n"
            "    ('GET', '/health'): health,\n"
            "}\n\n"
            "def dispatch(method: str, path: str, request: dict) -> dict:\n"
            "    handler = routes.get((method, path), not_found)\n"
            "    return handler(request)\n"
            "```\n\n"
            "Try it in the REPL:\n\n"
            "```python\n"
            "dispatch('GET', '/health', {})   # {'status': 200, 'body': 'ok'}\n"
