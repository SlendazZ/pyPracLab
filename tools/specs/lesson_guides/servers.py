            "When a segment varies (`/users/42`), match with a pattern and capture groups:\n\n"
            "```python\n"
            "import re\n\n"
            "USER_PATTERN = re.compile(r'^/users/(?P<id>\\d+)$')\n\n"
            "def match_user(path: str) -> dict | None:\n"
            "    m = USER_PATTERN.match(path)\n"
            "    if m:\n"
            "        return {'id': int(m.group('id'))}\n"
