            "# Middleware and Request Lifecycle\n\n"
            "## Overview\n\n"
            "Middleware wraps request handling to add cross-cutting behavior: logging, "
            "authentication, CORS headers, compression, error pages. Each layer calls "
            "the next inner layer.\n\n"
            "Handlers should focus on business logic (create user, fetch order). "
            "Middleware handles concerns that apply to *many* routes — things you "
            "do not want to copy-paste into every handler.\n\n"
            "Imagine peeling an onion: the request travels inward through layers until "
            "it reaches your handler, then the response travels back out through the "
            "same layers in reverse order.\n\n"
            "## Onion model\n\n"
            "Picture nested wrappers. The request enters from the outside; the response "
            "unwinds back out:\n\n"
            "```\n"
            "Request  → [Logging] → [Auth] → [Router] → Handler\n"
            "Response ← [Logging] ← [Auth] ← [Router] ← Handler\n"
            "```\n\n"
            "Outer middleware sees the request first and the response last. That is why "
            "logging middleware can record total time including inner layers.\n\n"
            "Order matters:\n\n"
            "```\n"
            "1. Error handler (outermost — catches exceptions from everything inside)\n"
            "2. Logging / timing\n"
            "3. CORS (before response leaves)\n"
            "4. Authentication (attach user to request)\n"
            "5. Router + handler (innermost)\n"
            "```\n\n"
            "## Simple WSGI-style wrapper\n\n"
            "WSGI apps are callables `(environ, start_response) -> iterable`. "
            "Middleware is a function that takes an app and returns a new app:\n\n"
            "```python\n"
            "def logging_middleware(app):\n"
            "    def wrapper(environ, start_response):\n"
            "        method = environ.get('REQUEST_METHOD', 'GET')\n"
            "        path = environ.get('PATH_INFO', '/')\n"
            "        print(f'{method} {path}')\n"
            "        return app(environ, start_response)\n"
            "    return wrapper\n"
            "```\n\n"
            "Stack middleware by nesting — outermost runs first on the way in:\n\n"
            "```python\n"
            "app = logging_middleware(timing_middleware(core_app))\n"
            "```\n\n"
            "Reading the stack right-to-left: `core_app` is innermost; `logging_middleware` "
            "wraps everything and runs first when a request arrives.\n\n"
            "## Worked example: request log line with timing\n\n"
            "A practical logger records method, path, status, and duration:\n\n"
            "```python\n"
            "import time\n\n"
            "def request_log_middleware(app):\n"
            "    def wrapper(environ, start_response):\n"
            "        start = time.perf_counter()\n"
            "        method = environ.get('REQUEST_METHOD', 'GET')\n"
            "        path = environ.get('PATH_INFO', '/')\n"
            "        status_holder = []\n\n"
            "        def custom_start_response(status, headers, exc_info=None):\n"
            "            status_holder.append(status)\n"
            "            return start_response(status, headers, exc_info)\n\n"
            "        try:\n"
            "            result = app(environ, custom_start_response)\n"
            "        finally:\n"
            "            elapsed_ms = (time.perf_counter() - start) * 1000\n"
            "            code = status_holder[0].split()[0] if status_holder else '???'\n"
            "            print(f'{method} {path} {code} {elapsed_ms:.1f}ms')\n"
            "        return result\n"
            "    return wrapper\n"
            "```\n\n"
            "Step by step:\n"
            "1. Record start time before calling the inner app.\n"
            "2. Wrap `start_response` to capture the HTTP status code.\n"
