            "# HTTPX Basics\n\n"
            "## Overview\n\n"
            "HTTPX is a modern HTTP client for Python. If you have used `requests`, "
            "HTTPX will feel familiar — but it also supports **async** (`await client.get(...)`) "
            "and ships with testing tools that do not require a live network.\n\n"
            "Think of HTTPX as \"requests, plus async and built-in mocking.\" You still "
            "send a method and URL; you still get back a response with status, headers, "
            "and body. The API is intentionally similar so migration is easy.\n\n"
            "Real-world uses:\n\n"
            "- Calling REST APIs from scripts and microservices\n"
            "- Async services that fan out to many endpoints at once\n"
            "- Testing FastAPI/Starlette apps with `httpx.ASGITransport`\n"
            "- Unit tests that mock HTTP without hitting the internet\n"
            "- HTTP/2 connections to modern APIs (optional extra)\n\n"
            "Install with `pip install httpx`. For HTTP/2 support, add the optional "
            "`httpx[http2]` extra.\n\n"
            "## Your first GET request\n\n"
            "The one-liner pattern mirrors `requests` exactly:\n\n"
            "```python\n"
            "import httpx\n\n"
            "response = httpx.get('https://httpbin.org/get', timeout=10.0)\n"
            "response.raise_for_status()   # raise if 4xx or 5xx\n"
            "data = response.json()\n"
            "print(data['url'])            # echo of the URL you requested\n"
            "```\n\n"
            "Step by step:\n"
            "1. `httpx.get(...)` sends an HTTP GET and waits for the response.\n"
            "2. `raise_for_status()` turns HTTP errors into exceptions you can catch.\n"
            "3. `.json()` parses the body as JSON (raises if the body is not JSON).\n\n"
            "**Always set `timeout=`** in production. Without it, a hung server blocks "
            "your worker forever.\n\n"
            "## Sync client: GET and POST\n\n"
