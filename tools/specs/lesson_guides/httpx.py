            "## Building requests without sending\n\n"
            "Construct a `Request` object and inspect it before any network I/O. "
            "Ideal for unit tests that verify URL construction:\n\n"
            "```python\n"
            "import httpx\n\n"
            "req = httpx.Request(\n"
            "    'GET',\n"
            "    'https://api.example.com/search',\n"
            "    params={'q': 'python', 'page': 2},\n"
            ")\n"
