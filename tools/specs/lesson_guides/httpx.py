            "## Async client\n\n"
            "When you need to call many APIs concurrently, async saves wall-clock time:\n\n"
            "```python\n"
            "import asyncio\n"
            "import httpx\n\n"
            "async def fetch_all(urls: list[str]) -> list[dict]:\n"
            "    async with httpx.AsyncClient(timeout=10.0) as client:\n"
            "        tasks = [client.get(url) for url in urls]\n"
            "        responses = await asyncio.gather(*tasks)\n"
            "        for r in responses:\n"
            "            r.raise_for_status()\n"
            "        return [r.json() for r in responses]\n\n"
            "results = asyncio.run(fetch_all([\n"
            "    'https://api.example.com/users/1',\n"
            "    'https://api.example.com/users/2',\n"
            "    'https://api.example.com/users/3',\n"
