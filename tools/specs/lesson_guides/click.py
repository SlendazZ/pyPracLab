            "Where `main` is your `@click.group()` or `@click.command()` function. "
            "After `pip install -e .`, users run `mytool --help` from anywhere.\n\n"
            "## Common pitfalls\n\n"
            "- Forgetting `@click.command()` on the function you invoke\n"
            "- Option names must use `--kebab-case` or `--snake_case`, not spaces\n"
            "- Using `print()` instead of `click.echo()` (encoding issues on Windows)\n"
            "- Testing via subprocess instead of `CliRunner` (slow and brittle)\n"
            "- Boolean flags: use `is_flag=True`, not `type=bool` (Click treats "
            "`--flag False` oddly with `type=bool`)\n"
            "- Decorator order: `@click.command()` goes **below** `@click.option()` "
            "(options closest to the function)\n"
            "- Mutable default arguments in command callbacks — pass via option, "
            "not as function default list/dict\n\n"
