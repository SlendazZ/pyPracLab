            "```\n\n"
            "## Common pitfalls\n\n"
            "- Mutable defaults on model fields — use `Field(default_factory=list)` "
            "for lists/dicts\n"
            "- Returning ORM objects without `from_attributes=True`\n"
            "- One giant model for everything — split Create/Update/Out variants\n"
            "- Validating the same rules in the route *and* the model (DRY violation)\n"
            "- Forgetting `response_model` and leaking sensitive columns\n"
            "- Using Pydantic v1 methods (`.dict()`) in a v2 project\n\n"
            "## Practice\n\n"
