            "# Pydantic Models and Validation\n\n"
            "## Overview\n\n"
            "Pydantic models describe the shape of request and response data. FastAPI "
            "uses them to parse JSON bodies, validate fields, and generate OpenAPI "
            "schemas — your docs stay in sync with your code automatically.\n\n"
            "Think of a Pydantic model as a contract: \"this endpoint accepts exactly "
            "these fields, with these types and constraints.\"\n\n"
            "## Defining a model\n\n"
            "```python\n"
            "from pydantic import BaseModel, EmailStr, Field\n\n"
            "class UserCreate(BaseModel):\n"
            "    email: EmailStr\n"
            "    name: str = Field(min_length=1, max_length=100)\n"
            "    age: int | None = None\n"
            "```\n\n"
            "Field constraints produce clear 422 errors when violated:\n\n"
            "```json\n"
            "{\n"
            "  \"detail\": [\n"
            "    {\"loc\": [\"body\", \"email\"], \"msg\": \"value is not a valid email address\"}\n"
            "  ]\n"
            "}\n"
            "```\n\n"
            "Common validators:\n\n"
            "```python\n"
            "from pydantic import Field\n\n"
            "price: float = Field(gt=0, description='Must be positive')\n"
            "tags: list[str] = Field(default_factory=list, max_length=10)\n"
            "role: str = Field(pattern=r'^(admin|user|guest)$')\n"
            "```\n\n"
            "## Using models in routes\n\n"
            "```python\n"
            "from fastapi import FastAPI, status\n\n"
            "app = FastAPI()\n\n"
            "@app.post('/users', status_code=status.HTTP_201_CREATED)\n"
            "def create_user(user: UserCreate):\n"
            "    # user is already validated — safe to use\n"
            "    return {'id': 1, **user.model_dump()}\n"
            "```\n\n"
            "Send JSON from a client or TestClient:\n\n"
            "```python\n"
            "client.post('/users', json={'email': 'ada@example.com', 'name': 'Ada'})\n"
            "```\n\n"
            "Invalid body → automatic 422 with per-field errors. Your handler never runs.\n\n"
            "## Worked example: separate create and response shapes\n\n"
            "Clients send passwords; responses should not echo them:\n\n"
            "```python\n"
            "class UserCreate(BaseModel):\n"
            "    email: EmailStr\n"
            "    password: str = Field(min_length=8)\n\n"
            "class UserOut(BaseModel):\n"
            "    id: int\n"
            "    email: EmailStr\n\n"
            "@app.post('/users', response_model=UserOut, status_code=201)\n"
            "def create_user(user: UserCreate):\n"
            "    stored = save_user(user)  # hashes password internally\n"
            "    return stored  # extra fields like password_hash are stripped\n"
            "```\n\n"
            "Even if `save_user` returns `password_hash`, `response_model=UserOut` "
            "filters it out of the JSON response.\n\n"
            "## Response models\n\n"
            "`response_model=` filters and validates what leaves your API:\n\n"
            "```python\n"
            "class UserOut(BaseModel):\n"
            "    id: int\n"
            "    email: EmailStr\n"
            "    name: str\n\n"
            "@app.get('/users/{uid}', response_model=UserOut)\n"
            "def get_user(uid: int):\n"
            "    row = db.get_user(uid)\n"
            "    return row  # Pydantic drops extra keys, coerces types\n"
            "```\n\n"
            "This prevents accidentally leaking internal fields (`password_hash`, "
            "`internal_notes`) in JSON responses.\n\n"
            "## model_dump and model_validate (Pydantic v2)\n\n"
            "```python\n"
            "user = UserCreate(email='a@b.com', name='Ada', password='long-enough')\n"
            "data = user.model_dump()                    # dict for JSON/DB insert\n"
            "safe = user.model_dump(exclude={'password'})  # omit sensitive fields\n"
            "restored = UserCreate.model_validate({'email': 'a@b.com', 'name': 'Ada', 'password': 'long-enough'})\n"
            "```\n\n"
            "Pydantic v1 used `.dict()` and `.parse_obj()` — check your project's "
            "version in `requirements.txt` or `pyproject.toml`.\n\n"
            "## Nested and optional models\n\n"
            "```python\n"
            "class Address(BaseModel):\n"
            "    street: str\n"
            "    city: str\n\n"
            "class UserCreate(BaseModel):\n"
            "    name: str\n"
            "    address: Address | None = None\n"
            "```\n\n"
            "FastAPI validates nested objects recursively:\n\n"
            "```python\n"
            "client.post('/users', json={\n"
            "    'name': 'Ada',\n"
            "    'address': {'street': '1 Analytical Engine Rd', 'city': 'London'},\n"
            "})\n"
            "```\n\n"
            "## Sharing fields with inheritance\n\n"
            "```python\n"
            "class UserBase(BaseModel):\n"
            "    email: EmailStr\n"
            "    name: str\n\n"
            "class UserCreate(UserBase):\n"
            "    password: str\n\n"
            "class UserUpdate(BaseModel):\n"
            "    name: str | None = None\n"
            "    email: EmailStr | None = None\n\n"
            "class UserOut(UserBase):\n"
            "    id: int\n"
            "    model_config = {'from_attributes': True}\n"
            "```\n\n"
            "Create / Update / Out variants keep schemas DRY.\n\n"
            "## Config for ORM objects\n\n"
            "When returning SQLAlchemy or other ORM rows:\n\n"
            "```python\n"
            "class UserOut(BaseModel):\n"
            "    model_config = {'from_attributes': True}\n"
            "    id: int\n"
            "    email: str\n"
            "```\n\n"
            "`from_attributes=True` lets Pydantic read attributes from arbitrary objects, "
            "not just dicts.\n\n"
            "## Real-world scenario: webhook payload\n\n"
            "```python\n"
            "class StripeEvent(BaseModel):\n"
            "    id: str\n"
            "    type: str\n"
            "    data: dict\n\n"
            "@app.post('/webhooks/stripe')\n"
            "def stripe_webhook(event: StripeEvent):\n"
            "    if event.type == 'payment_intent.succeeded':\n"
            "        handle_payment(event.data)\n"
            "    return {'received': True}\n"
            "```\n\n"
            "Pydantic rejects malformed webhooks before your handler processes them.\n\n"
            "## Testing validation\n\n"
            "```python\n"
            "def test_create_user_ok():\n"
            "    r = client.post('/users', json={'email': 'ada@ex.com', 'name': 'Ada', 'password': 'secret123'})\n"
            "    assert r.status_code == 201\n"
            "    body = r.json()\n"
