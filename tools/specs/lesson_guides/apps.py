            "# Classes and Application State\n\n"
            "## Overview\n\n"
            "A class bundles data (attributes) and behavior (methods) for one concept: "
            "a todo list, shopping cart, game board. Application logic becomes easier "
            "to test when state lives in well-named objects.\n\n"
            "Without classes, a growing script accumulates loose variables and functions "
            "that pass the same dict everywhere. A class says: this data and these "
            "operations belong together. Callers interact through a small public API "
            "instead of reaching into raw lists and indexes.\n\n"
            "If you can describe something as a noun with verbs — a cart that adds items, "
            "a list that completes tasks — it is probably a class candidate.\n\n"
            "## Minimal class\n\n"
            "Every instance method takes `self` as the first parameter — the object "
            "being operated on. `__init__` runs when you construct a new instance:\n\n"
            "```python\n"
            "class TodoList:\n"
            "    def __init__(self):\n"
            "        self.items = []\n\n"
            "    def add(self, task: str) -> None:\n"
            "        self.items.append(task)\n\n"
            "    def done(self, index: int) -> str:\n"
            "        return self.items.pop(index)\n"
            "```\n\n"
            "Usage:\n\n"
            "```python\n"
            "todos = TodoList()\n"
            "todos.add('buy milk')\n"
            "todos.add('write tests')\n"
            "finished = todos.done(0)   # removes and returns 'buy milk'\n"
            "```\n\n"
            "`self.items` is per-instance state. Two `TodoList` objects do not share the "
            "same list unless you deliberately wire them that way:\n\n"
            "```python\n"
            "work = TodoList()\n"
            "home = TodoList()\n"
            "work.add('deploy')\n"
            "len(home.items)   # 0 — separate lists\n"
            "```\n\n"
            "## Worked example: a complete TodoList\n\n"
            "A more realistic version validates input and exposes read-only views:\n\n"
            "```python\n"
            "class TodoList:\n"
            "    def __init__(self):\n"
            "        self._items: list[str] = []\n\n"
            "    def add(self, task: str) -> None:\n"
            "        task = task.strip()\n"
            "        if not task:\n"
            "            raise ValueError('task cannot be empty')\n"
            "        self._items.append(task)\n\n"
            "    def list(self) -> list[str]:\n"
            "        return list(self._items)\n\n"
            "    def complete(self, index: int) -> str:\n"
            "        if index < 0 or index >= len(self._items):\n"
            "            raise IndexError('no task at that index')\n"
            "        return self._items.pop(index)\n\n"
            "    def __len__(self) -> int:\n"
            "        return len(self._items)\n\n"
            "    def __repr__(self) -> str:\n"
            "        return f'TodoList({self._items!r})'\n"
            "```\n\n"
            "Step by step:\n"
            "1. `_items` with a leading underscore signals internal storage (convention, not enforcement).\n"
            "2. `add` strips whitespace and rejects empty strings.\n"
            "3. `list` returns a copy so callers cannot mutate internal state directly.\n"
            "4. `complete` bounds-checks before `pop`.\n"
            "5. `__len__` enables `len(todos)`; `__repr__` helps debugging.\n\n"
            "Quick test in the REPL:\n\n"
            "```python\n"
            "t = TodoList()\n"
            "t.add('read chapter 3')\n"
            "assert len(t) == 1\n"
            "assert t.list() == ['read chapter 3']\n"
            "t.complete(0)\n"
            "assert len(t) == 0\n"
            "```\n\n"
            "Why return a copy from `list()`?\n\n"
            "```python\n"
            "t = TodoList()\n"
            "t.add('task')\n"
            "view = t.list()\n"
            "view.clear()        # does NOT empty the TodoList\n"
            "len(t)              # still 1\n"
            "```\n\n"
            "## Encapsulation convention\n\n"
            "Python trusts the programmer — conventions over strict privacy:\n\n"
            "- Public API: no leading underscore (`add`, `complete`)\n"
            "- Internal helper or storage: `_validate()`, `_items`\n"
            "- Name mangling: `__secret` (rare; avoid unless subclass interference is a real problem)\n\n"
            "Callers should use public methods. If you need to change how tasks are stored "
            "(list → database), you change `_items` internals without breaking `add`/`complete`.\n\n"
            "## When to use a class vs functions\n\n"
            "| Use a class when…              | Use functions when…     |\n"
            "|--------------------------------|-------------------------|\n"
            "| State persists across calls    | One-off transform       |\n"
            "| Multiple related operations    | Single pure computation |\n"
            "| You need several instances     | No shared state         |\n\n"
            "Example: `normalize_email(s)` is a function; `ShoppingCart` is a class because "
            "it accumulates items across many `add` calls.\n\n"
            "Worked example: shopping cart sketch:\n\n"
            "```python\n"
            "class ShoppingCart:\n"
            "    def __init__(self):\n"
            "        self._lines: list[tuple[str, int]] = []\n\n"
            "    def add(self, name: str, price_cents: int) -> None:\n"
            "        self._lines.append((name, price_cents))\n\n"
            "    def total_cents(self) -> int:\n"
            "        return sum(price for _, price in self._lines)\n\n"
            "cart = ShoppingCart()\n"
            "cart.add('notebook', 499)\n"
            "cart.add('pen', 199)\n"
            "cart.total_cents()   # 698\n"
            "```\n\n"
            "## Dataclasses for data-heavy objects\n\n"
            "When a class is mostly fields with little behavior, `@dataclass` removes "
            "boilerplate:\n\n"
            "```python\n"
            "from dataclasses import dataclass\n\n"
            "@dataclass\n"
            "class Task:\n"
            "    title: str\n"
            "    done: bool = False\n\n"
            "task = Task('deploy')\n"
            "task.done = True\n"
            "```\n\n"
            "Dataclasses auto-generate `__init__`, `__repr__`, and equality. Use a regular "
            "class when behavior dominates; use dataclass for structured records.\n\n"
            "Combine dataclass records with a class that owns a collection:\n\n"
            "```python\n"
            "from dataclasses import dataclass\n\n"
            "@dataclass\n"
            "class Task:\n"
            "    title: str\n"
            "    done: bool = False\n\n"
            "class TaskBoard:\n"
            "    def __init__(self):\n"
            "        self._tasks: list[Task] = []\n\n"
            "    def add(self, title: str) -> None:\n"
            "        self._tasks.append(Task(title))\n\n"
            "    def pending(self) -> list[Task]:\n"
            "        return [t for t in self._tasks if not t.done]\n"
            "```\n\n"
            "## Common pitfalls\n\n"
            "- Mutable default args in `__init__` — use `None` and assign in body:\n\n"
            "```python\n"
            "def __init__(self, items=None):\n"
            "    self.items = [] if items is None else list(items)\n"
            "```\n\n"
            "Wrong version `def __init__(self, items=[])` shares one list across all instances.\n\n"
            "- God objects that know everything — split responsibilities (storage vs UI)\n"
            "- Forgetting `self.` when assigning instance attributes\n"
            "- Returning the internal list directly — callers can `.clear()` your state\n"
            "- Putting I/O (print, input) inside domain classes — keep them testable\n\n"
            "## Practice\n\n"
            "Implement a TodoList with add, list, and complete operations.\n\n"
            "## Summary\n\n"
            "Classes model things your app manipulates over time. Keep them small; "
            "test methods in isolation. Use `__init__` for setup, underscore conventions "
            "for internals, and return copies when exposing mutable state."
