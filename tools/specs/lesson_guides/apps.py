            "# State Machines in Applications\n\n"
            "## Overview\n\n"
            "Many app workflows are finite state machines: orders (pending → paid → "
            "shipped), tickets (open → in progress → closed), games (menu → playing → "
            "game over). Explicit states prevent impossible transitions.\n\n"
            "A boolean flag per concern (`is_paid`, `is_shipped`, `is_cancelled`) "
            "explodes into illegal combinations — paid and cancelled at once. A state "
            "machine allows exactly one current state and defines which moves are legal.\n\n"
            "Think of states as rooms and transitions as doors. You can only walk through "
            "doors that exist; the transition table is the floor plan.\n\n"
            "## Modeling states\n\n"
            "`Enum` gives named constants and blocks typos like `'PENDNG'`:\n\n"
            "```python\n"
            "from enum import Enum, auto\n\n"
            "class OrderState(Enum):\n"
            "    PENDING = auto()\n"
            "    PAID = auto()\n"
            "    SHIPPED = auto()\n"
            "    CANCELLED = auto()\n"
            "```\n\n"
            "Compare with `is` or `==` — never compare to raw strings loaded from a database "
            "without validating first:\n\n"
            "```python\n"
            "def parse_state(name: str) -> OrderState:\n"
            "    try:\n"
            "        return OrderState[name]\n"
            "    except KeyError:\n"
            "        raise ValueError(f'unknown state: {name}')\n"
            "```\n\n"
            "## Transition table\n\n"
            "Encode allowed edges in one data structure. Every transition goes through one function:\n\n"
            "```python\n"
            "ALLOWED = {\n"
            "    OrderState.PENDING: {OrderState.PAID, OrderState.CANCELLED},\n"
            "    OrderState.PAID: {OrderState.SHIPPED},\n"
            "    OrderState.SHIPPED: set(),\n"
            "    OrderState.CANCELLED: set(),\n"
            "}\n\n"
            "def transition(order, new_state: OrderState) -> None:\n"
            "    allowed = ALLOWED[order.state]\n"
            "    if new_state not in allowed:\n"
            "        raise ValueError(\n"
            "            f'cannot go {order.state.name} -> {new_state.name}'\n"
            "        )\n"
            "    order.state = new_state\n"
            "```\n\n"
            "Terminal states (`SHIPPED`, `CANCELLED`) have empty allowed sets — no way out "
            "without adding a new business rule.\n\n"
            "Draw the table as a diagram while learning:\n\n"
            "```\n"
            "PENDING ──→ PAID ──→ SHIPPED\n"
            "   │\n"
            "   └──→ CANCELLED\n"
            "```\n\n"
            "## Worked example: Order object with history\n\n"
            "Wrap state in a class so callers cannot assign `order.state` directly:\n\n"
            "```python\n"
            "from dataclasses import dataclass, field\n"
            "from enum import Enum, auto\n\n"
            "class OrderState(Enum):\n"
            "    PENDING = auto()\n"
            "    PAID = auto()\n"
            "    SHIPPED = auto()\n"
            "    CANCELLED = auto()\n\n"
            "ALLOWED = {\n"
            "    OrderState.PENDING: {OrderState.PAID, OrderState.CANCELLED},\n"
            "    OrderState.PAID: {OrderState.SHIPPED},\n"
            "    OrderState.SHIPPED: set(),\n"
            "    OrderState.CANCELLED: set(),\n"
            "}\n\n"
            "@dataclass\n"
            "class Order:\n"
            "    id: int\n"
            "    state: OrderState = OrderState.PENDING\n"
            "    history: list[str] = field(default_factory=list)\n\n"
            "    def advance(self, new_state: OrderState) -> None:\n"
            "        if new_state not in ALLOWED[self.state]:\n"
            "            raise ValueError(f'{self.state.name} -> {new_state.name} not allowed')\n"
            "        self.history.append(f'{self.state.name} -> {new_state.name}')\n"
            "        self.state = new_state\n"
            "```\n\n"
            "Walk through a happy path:\n\n"
            "```python\n"
            "order = Order(id=1)\n"
            "order.advance(OrderState.PAID)\n"
            "order.advance(OrderState.SHIPPED)\n"
            "order.history\n"
            "# ['PENDING -> PAID', 'PAID -> SHIPPED']\n\n"
            "order.advance(OrderState.CANCELLED)  # ValueError\n"
            "```\n\n"
            "Step by step:\n"
            "1. New orders start in `PENDING`.\n"
            "2. `advance` checks the table before mutating.\n"
            "3. `history` records an audit trail for support and debugging.\n"
            "4. Illegal moves raise immediately instead of corrupting data.\n\n"
            "List legal next moves for UI buttons:\n\n"
            "```python\n"
            "def available_actions(order: Order) -> list[OrderState]:\n"
            "    return sorted(ALLOWED[order.state], key=lambda s: s.name)\n\n"
            "available_actions(Order(id=1))\n"
            "# [OrderState.PAID, OrderState.CANCELLED]\n"
            "```\n\n"
            "## Worked example: timer states (Pomodoro-style)\n\n"
            "The same pattern fits UI timers with fewer states:\n\n"
            "```python\n"
            "from enum import Enum, auto\n\n"
            "class TimerPhase(Enum):\n"
            "    IDLE = auto()\n"
            "    RUNNING = auto()\n"
            "    PAUSED = auto()\n"
            "    FINISHED = auto()\n\n"
            "TIMER_ALLOWED = {\n"
            "    TimerPhase.IDLE: {TimerPhase.RUNNING},\n"
            "    TimerPhase.RUNNING: {TimerPhase.PAUSED, TimerPhase.FINISHED},\n"
            "    TimerPhase.PAUSED: {TimerPhase.RUNNING, TimerPhase.IDLE},\n"
            "    TimerPhase.FINISHED: {TimerPhase.IDLE},\n"
            "}\n\n"
            "def advance_timer(phase: TimerPhase, event: str) -> TimerPhase:\n"
            "    targets = {\n"
            "        'start': TimerPhase.RUNNING,\n"
            "        'pause': TimerPhase.PAUSED,\n"
            "        'finish': TimerPhase.FINISHED,\n"
            "        'reset': TimerPhase.IDLE,\n"
            "    }\n"
            "    new = targets[event]\n"
            "    if new not in TIMER_ALLOWED[phase]:\n"
            "        raise ValueError(f'cannot {event} while {phase.name}')\n"
            "    return new\n"
            "```\n\n"
            "Map button clicks to events (`start`, `pause`, `reset`) and only enable "
            "buttons when the event is legal from the current phase.\n\n"
            "Walk through a session:\n\n"
            "```python\n"
            "phase = TimerPhase.IDLE\n"
            "phase = advance_timer(phase, 'start')    # RUNNING\n"
            "phase = advance_timer(phase, 'pause')    # PAUSED\n"
            "phase = advance_timer(phase, 'start')    # RUNNING again\n"
            "phase = advance_timer(phase, 'finish')   # FINISHED\n"
            "phase = advance_timer(phase, 'reset')    # IDLE\n"
            "advance_timer(phase, 'pause')            # ValueError — cannot pause while IDLE\n"
            "```\n\n"
            "## Benefits\n\n"
            "- Business rules live in one place (the transition table)\n"
            "- Easy to diagram and review with stakeholders\n"
            "- Tests cover every allowed and forbidden edge in a loop\n\n"
            "```python\n"
            "def test_all_illegal_transitions():\n"
            "    for src in OrderState:\n"
            "        for dst in OrderState:\n"
            "            if dst not in ALLOWED[src]:\n"
            "                order = Order(id=0, state=src)\n"
            "                try:\n"
            "                    order.advance(dst)\n"
            "                except ValueError:\n"
            "                    pass\n"
            "                else:\n"
            "                    raise AssertionError(f'{src} -> {dst} should fail')\n"
            "```\n\n"
            "This loop tests every forbidden edge automatically — add a new state and "
            "the test still covers it.\n\n"
            "## Persisting state safely\n\n"
            "When saving to a database or file, store the enum name as a string:\n\n"
            "```python\n"
            "def order_to_dict(order: Order) -> dict:\n"
            "    return {'id': order.id, 'state': order.state.name, 'history': order.history}\n\n"
            "def order_from_dict(data: dict) -> Order:\n"
            "    return Order(\n"
            "        id=data['id'],\n"
            "        state=OrderState[data['state']],\n"
            "        history=list(data.get('history', [])),\n"
            "    )\n"
            "```\n\n"
            "Always validate on load — never trust raw strings from storage.\n\n"
            "## UI connection\n\n"
            "Enable buttons only for valid transitions. Display current state clearly — "
            "users should never wonder whether an order is paid or merely pending.\n\n"
            "```python\n"
            "def button_enabled(order: Order, action: OrderState) -> bool:\n"
            "    return action in ALLOWED[order.state]\n"
            "```\n\n"
            "## Common pitfalls\n\n"
            "- Scattering `if status == ...` across dozens of files\n"
            "- Allowing backward transitions without explicit business approval\n"
            "- Persisting state as free-form strings without validation on load\n"
            "- Skipping terminal states in the table (implicit None → anything)\n"
            "- Letting callers set `.state` directly — route changes through `advance`\n\n"
            "## Practice\n\n"
            "Implement order state transitions with a validation table.\n\n"
            "## Summary\n\n"
            "State machine = states + allowed edges + transition function. Use when "
