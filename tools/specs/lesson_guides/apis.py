            "1. `urlparse` splits the URL into components.\n"
            "2. `parse_qs` reads existing query keys (values are lists).\n"
            "3. Dict merge lets new params override old ones.\n"
            "4. `urlencode` + `urlunparse` rebuilds a valid URL.\n\n"
            "Trace the merge:\n\n"
            "```python\n"
            "print(merged)\n"
            "# https://api.example.com/items?limit=20&tag=api&page=2\n"
            "```\n\n"
            "For greenfield URLs without existing params:\n\n"
            "```python\n"
            "from urllib.parse import urlencode\n\n"
            "def build_list_url(base: str, **filters) -> str:\n"
            "    clean = {k: v for k, v in filters.items() if v is not None}\n"
            "    if not clean:\n"
            "        return base\n"
            "    return f'{base}?{urlencode(clean)}'\n\n"
            "build_list_url('https://api.example.com/items', limit=20, tag='python')\n"
            "# https://api.example.com/items?limit=20&tag=python\n"
            "build_list_url('https://api.example.com/items', limit=20, tag=None)\n"
            "# https://api.example.com/items?limit=20  — None keys dropped\n"
            "```\n\n"
            "## Idempotency and method choice\n\n"
            "GET, PUT, and DELETE should be safe to repeat — calling them twice should "
            "not create duplicate side effects. POST creates new resources each time.\n\n"
            "```\n"
            "GET    /items/7     → read (safe, repeatable)\n"
