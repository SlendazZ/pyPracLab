            "# REST API Design Basics\n\n"
            "## Overview\n\n"
            "REST (Representational State Transfer) organizes APIs around resources "
            "(nouns) and HTTP verbs (actions). Good design is predictable: the same "
            "patterns everywhere.\n\n"
            "When you read a well-designed API, you should guess the next endpoint "
            "before reading the docs: plural nouns, standard methods, consistent JSON "
            "shapes, and status codes that match what actually happened.\n\n"
            "Bad APIs mix verbs into URLs, return 200 for everything, and rename fields "
            "between endpoints. Good APIs feel boring — in a good way.\n\n"
            "## Resource-oriented URLs\n\n"
            "Paths name *things*, not actions. HTTP methods carry the verb:\n\n"
            "```\n"
            "GET    /users          list users\n"
            "POST   /users          create user\n"
            "GET    /users/42       get user 42\n"
            "PATCH  /users/42       update fields\n"
            "DELETE /users/42       remove user\n"
            "GET    /users/42/posts nested collection\n"
            "```\n\n"
            "Use plural nouns (`/users`, not `/user`). Avoid verbs in paths — "
            "`/getUser` and `/createOrder` fight the HTTP method model.\n\n"
            "IDs identify one resource. Collections live at the plural path:\n\n"
            "```\n"
            "GET /items        → [{\"id\": 1, ...}, {\"id\": 2, ...}]\n"
            "GET /items/7      → {\"id\": 7, ...}\n"
            "```\n\n"
            "Nested resources express ownership:\n\n"
            "```\n"
            "GET  /users/42/orders       list orders for user 42\n"
            "POST /users/42/orders       create order for user 42\n"
            "GET  /orders/99/items       line items on order 99\n"
            "```\n\n"
            "Do not nest more than two levels deep (`/a/b/c` is usually a smell).\n\n"
            "## Status codes that match intent\n\n"
            "Return the code that describes the outcome, not always 200:\n\n"
            "- 200 OK — successful GET, PATCH, or PUT with a body\n"
            "- 201 Created + `Location` header on POST\n"
            "- 204 No Content on DELETE success (no body needed)\n"
            "- 400 Bad Request for validation errors (bad JSON, missing field)\n"
            "- 404 Not Found when the resource ID does not exist\n"
            "- 409 Conflict for duplicate unique keys (email already taken)\n\n"
            "Example POST response headers:\n\n"
            "```\n"
            "HTTP/1.1 201 Created\n"
            "Location: /users/42\n"
            "Content-Type: application/json\n"
            "\n"
            "{\"id\": 42, \"email\": \"ada@example.com\"}\n"
            "```\n\n"
            "Worked example: choose the right code in a tiny handler:\n\n"
            "```python\n"
            "def delete_user(user_id: int, db: dict) -> tuple[int, str | None]:\n"
            "    if user_id not in db:\n"
            "        return 404, '{\"error\": \"not_found\"}'\n"
            "    del db[user_id]\n"
            "    return 204, None   # success, no body\n"
            "```\n\n"
            "## Request and response shapes\n\n"
            "Keep field names consistent across every endpoint — pick snake_case or "
            "camelCase and stick with it:\n\n"
            "```json\n"
            "{\n"
            "  \"id\": 42,\n"
            "  \"email\": \"ada@example.com\",\n"
            "  \"created_at\": \"2026-01-15T10:00:00Z\"\n"
            "}\n"
            "```\n\n"
            "Timestamps as ISO-8601 strings (`2026-01-15T10:00:00Z`) parse cleanly in "
            "every language. Booleans are `true`/`false`, not `1`/`0`.\n\n"
            "List responses wrap items plus metadata:\n\n"
            "```json\n"
            "{\n"
            "  \"items\": [\n"
            "    {\"id\": 1, \"name\": \"Widget\"},\n"
            "    {\"id\": 2, \"name\": \"Gadget\"}\n"
            "  ]\n"
            "}\n"
            "```\n\n"
            "Error bodies should be machine- and human-readable:\n\n"
            "```json\n"
            "{\n"
            "  \"error\": \"validation_failed\",\n"
            "  \"message\": \"email is required\",\n"
            "  \"fields\": {\"email\": \"missing\"}\n"
            "}\n"
            "```\n\n"
            "## Pagination and filtering\n\n"
            "List endpoints accept query parameters instead of new paths:\n\n"
            "```\n"
            "GET /items?limit=20&offset=40\n"
            "GET /items?sort=-created_at&tag=python\n"
            "GET /items?status=active&min_price=10\n"
            "```\n\n"
            "Return metadata so clients know how to fetch the next page:\n\n"
            "```json\n"
            "{\n"
            "  \"items\": [...],\n"
            "  \"total\": 150,\n"
            "  \"limit\": 20,\n"
            "  \"offset\": 40\n"
            "}\n"
            "```\n\n"
            "Client logic for paging through all results:\n\n"
            "```python\n"
            "def fetch_all_items(client, base_url: str, limit: int = 50) -> list[dict]:\n"
            "    offset = 0\n"
            "    all_items = []\n"
            "    while True:\n"
            "        url = f'{base_url}?limit={limit}&offset={offset}'\n"
            "        page = client.get(url).json()\n"
            "        all_items.extend(page['items'])\n"
            "        offset += limit\n"
            "        if offset >= page['total']:\n"
            "            break\n"
            "    return all_items\n"
            "```\n\n"
            "## Worked example: merge and build query strings\n\n"
            "Clients often combine default filters with user-supplied ones. Merge dicts, "
            "then encode safely:\n\n"
            "```python\n"
            "from urllib.parse import urlencode, urlparse, parse_qs, urlunparse\n\n"
            "def merge_query_params(url: str, extra: dict) -> str:\n"
            "    parts = urlparse(url)\n"
            "    existing = {k: v[0] for k, v in parse_qs(parts.query).items()}\n"
            "    merged = {**existing, **extra}\n"
            "    new_query = urlencode(merged)\n"
            "    return urlunparse(parts._replace(query=new_query))\n\n"
            "base = 'https://api.example.com/items?limit=20&tag=python'\n"
            "merged = merge_query_params(base, {'page': 2, 'tag': 'api'})\n"
            "# tag=api overwrites tag=python; page=2 is added\n"
            "```\n\n"
